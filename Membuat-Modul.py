import math

def volumeKubus (R) :
    return R * R * R
    
def luasKubus (R) :
    return 6 * R * R
 
def volumeBola (r) :
    return 4/3 * math.pi * r * r
    
def luasBola (r) :
    return 4 * math.pi * r * r
    
def volumeTabung (r, t) :
    return math.pi * r * r * t
    
def luasSelimutTabung (r, t) :
    return 2 * math.pi * r * t
